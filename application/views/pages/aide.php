<div id="title">
Aide et foire aux questions
</div>
<ul type="square">
<li><b>Comment peut-on envoyer un message privé à un autre membres du site?</b><br>
Visitez simplement son profil, et cliquez sur "Envoyer un message".<br>&nbsp;</li>
<li><b>Comment consulter les messages reçus?</b><br>
En haut de la page, il y a un lien "Messages Privés" qui contient tous les messages reçus.<br>&nbsp;</li>
<li><b>Comment puis-je faire une recherche par âge, région etc.?</b><br>
En haut de chaque page, il est indiqué "Recherche rencontre". Sélectionnez homme ou femme, l'âge, la région, puis cliquez "recherchez".<br>&nbsp;</li>
<li><b>Comment puis-je ajouter ma photo?</b><br>
Allez dans la rubrique "<a href="<?php echo base_url('form/inscription_upload_image'); ?>">Ajouter photos</a>". Cliquez sur "parcourir", choisissez une photo sur votre ordinateur, et cliquez "soumettre".<br>&nbsp;</li>
<li><b>Pourquoi ma photo est-elle refusée?</b><br>
Vous n'avez pas respecté une règle dans "<a href="<?php echo base_url('form/inscription_upload_image'); ?>">Ajouter photos</a>" ou bien votre photo est trop lourde. Si votre photo est trop lourde, il faudra alors la réduire dans un éditeur d'images quelconque.<br>&nbsp;</li>
<li><b>J'ai envoyé un message privé à un autre membre, pourquoi je n'ai pas eu de réponse?</b><br>
Voici les possibilités:<br>
-Vous n'avez pas de photo dans votre profil.<br>
-Votre profil n'est pas rempli adéquatement. Assurez-vous qu'il soit complet, intéressant et original.<br>
-Vous communiquez votre email dans le premier message.<br>
-Votre premier message est incomplet. Par exemple: "Salut", "Allo" ou autre.<br>
-Vous copiez et collez le même message à plusieurs membres un après l'autre.<br>
-Votre message n'a pas été lu. Par exemple, il est possible que la personne ne se soit pas connectée récemment.<br>
-Vous ne correspondez pas aux critères de ce membre.<br>&nbsp;</li>
<li><b>J'ai reçu un message que je considère comme grossier ou inapproprié, que puis-je faire?</b><br>
Vous pouvez cliquer sur "Bloquer ce membre" ce qui fera en sorte que vous ne recevrez plus aucun message de ce membre. Vous pouvez également cliquer sur "Message inapproprié" ce qui bloquera ce membre et enverra également un signalement concernant ce membre.<br>&nbsp;</li>
<li><b>J'ai vu une photo qui ne respecte pas les règles, que puis-je faire?</b><br>
Cliquez sur "Photo inappropriée" pour signaler une photo qui ne respecte pas les règles.<br>&nbsp;</li>
<li><b>Je ne désire pas recevoir de message d'un membre, que faire?</b><br>
Cliquez sur "Bloquer ce membre".<br>&nbsp;</li>
<li><b>Pourquoi mes votes diminuent?</b><br>
En fait les votes ne diminuent pas. Nous gardons seulement les votes pour les 7 derniers jours.<br>&nbsp;</li>
<li><b>Comment puis-je supprimer mon compte?</b><br>
Allez dans "<a href="http://www.bewitu.com/editercompte.php">Editer votre compte</a>", puis suivez les instructions.<br>&nbsp;</li>
<li><b>Si je supprime mon compte, puis-je revenir?</b><br>
Si vous désirez quitter pendant un moment et avoir la possibilité de revenir sans difficulté, nous vous recommandons d'utiliser le "mode veille" qui est disponible dans "<a href="http://www.bewitu.com/editercompte.php">Editer votre compte</a>".<br>&nbsp;</li>
<li><b>Mon profil a été supprimé, pourquoi?</b><br>
Car une quelconque règle n'a pas été respectée. <a href="<?php echo base_url('regles-de-confidentialite');?>">Vous pouvez consulter les règles ici.</a><br>&nbsp;</li>
<li><b>Ma photo a été supprimée, pourquoi?</b><br>
Car une quelconque règle n'a pas été respectée. <a href="<?php echo base_url('form/inscription_upload_image'); ?>">Vous pouvez consulter les règles concernant les photos ici.</a><br>&nbsp;</li>
<li><b>Puis-je changer mon pseudo?</b><br>
Non, cela n'est pas possible.<br>&nbsp;</li>
<li><b>Où puis-je consulter les règles du site?</b><br>
Sur la page <a href="<?php echo base_url('regles-de-confidentialite');?>">Conditions d'utilisation</a>.<br>&nbsp;</li>
<li><b>J'ai perdu mon mot de passe, que faire?</b><br>
Allez sur cette page pour le récupérer: <a href="http://www.bewitu.com/motdepasseperdu.php">http://www.bewitu.com/motdepasseperdu.php</a>.<br>&nbsp;</li>
<li><b>J'aimerais changer mon mot de passe ou mon email, comment faire?</b><br>
Allez dans "<a href="http://www.bewitu.com/editercompte.php">Editer votre compte</a>".<br>&nbsp;</li>
<li><b>Comment désactiver les notices par emails?</b><br>
Allez dans "<a href="http://www.bewitu.com/editercompte.php">Editer votre compte</a>" et suivez les instructions.<br>&nbsp;</li>
<li><b>Comment faire pour ajouter des membres à ma liste d'amis?</b><br>
Visitez le profil d'un membre, puis cliquez sur le lien "Ajoutez à vos amis".<br>&nbsp;</li>
<li><b>Comment faire pour avoir des commentaires dans mon profil?</b><br>
Seules les personnes ajoutées dans votre liste d'amis peuvent laisser des commentaires en bas de votre profil.<br>&nbsp;</li>
<li><b>Je reçois des messages de personnes qui ne correspondent pas à mes critères, que puis-je faire?</b><br>
Vous pouvez configurer des filtres dans votre messagerie privée à partir de la page "<a href="http://www.bewitu.com/editercompte.php">Éditer votre compte</a>".<br>&nbsp;</li>
<li><b>Si je bloque un membre, ou je le signale, est-ce qu'il va en être informé?</b><br>
Non, il n'en sera pas informé.<br>&nbsp;</li>
<li><b>Je ne reçois pas beaucoup de messages privés, que puis-je faire?</b><br>
-Ajoutez plusieurs photos à votre profil.<br>
-Remplissez bien votre profil.<br>
-Remplissez votre entrevue.<br>
-Contactez des personnes qui vous intéressent et qui sont susceptibles d'être intéressées à vous.<br>
-Votez pour les profils qui vous intéressent.<br>&nbsp;</li>
</ul>
